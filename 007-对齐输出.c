// 007:对齐输出
/*
总时间限制: 1000ms 内存限制: 65536kB
描述
读入三个整数，按每个整数占8个字符的宽度，右对齐输出它们。

输入
只有一行，包含三个整数，整数之间以一个空格分开。
输出
只有一行，按照格式要求依次输出三个整数，之间以一个空格分开。
样例输入
123456789 0 -1
样例输出
123456789        0       -1
*/

#include <stdio.h>
int main()
{
    long a,b,c;
    scanf("%ld %ld %ld", &a, &b, &c);
    printf("%8ld %8ld %8ld\n", a, b, c);
}